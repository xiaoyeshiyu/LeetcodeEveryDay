func subsets(nums []int) [][]int {
	res := make([][]int, 0)
	length := len(nums)
	for i := 0; i < 1<<length; i++ {
		tmp := make([]int, 0, length)
		for j := 0; j < length; j++ {
			if i>>j&1 == 1 {
				tmp = append(tmp, nums[j])
			}
		}
		res = append(res, tmp)
	}
	return res
}

