func addNegabinary(arr1 []int, arr2 []int) []int {
	l1, l2 := len(arr1), len(arr2)
	ans := make([]int, 0, l1)
	for c := 0; l1 > 0 || l2 > 0||c != 0 ; {
		x := c
		if l1 > 0 {
			x += arr1[l1-1]
			l1--
		}
		if l2 > 0 {
			x += arr2[l2-1]
			l2--
		}
		c = 0
		if x >= 2 {
			c = -1
			x -= 2
		} else if x == -1 {
			x = 1
			c += 1
		}
		ans = append(ans, x)
	}

	for len(ans) > 1 && ans[len(ans)-1] == 0  {
		ans = ans[:len(ans)-1]
	}

	for i := 0; i < len(ans)>>1; i++ {
		ans[i],ans[len(ans)-1-i] = ans[len(ans)-1-i],ans[i]
	}
	return ans
}

